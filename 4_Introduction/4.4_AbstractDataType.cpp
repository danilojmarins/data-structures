// A datatype is determined in terms of: Representation
// of that data and Operation on that data. Representation
// is the way in which the data is represented in memory.
// Int: 4 bytes - 32 bits - 1 sign bit - 31 data bits.
// Operation are the operations allowed on certain datatype.
// Int: +, -, *, /, %, ++, --.
// Abstract can be defined as something that has it's internal
// details hidden. The sum operation is abstracted with the +
// so we don't need to worry how it is done in the bit level.
// Abstract DataType is a concept used in Object Oriented
// Programming, where the types of data of a class are hidden.